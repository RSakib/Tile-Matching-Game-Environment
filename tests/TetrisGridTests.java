    private void assertGridEquals(int[][] expected, Grid grid){
        for (int r = 0; r < expected.length; r++) {
            for (int c = 0; c < expected[r].length; c++) {
                Position p = new Position(r, c);
                if (expected[r][c] == 0) {
                    assertTrue("Incorrect at " + p, grid.tileAt(p) instanceof EmptyTile);
                } else if (expected[r][c] == 1) {
                    assertFalse("Incorrect at " + p, grid.tileAt(new Position(r, c)) instanceof EmptyTile);
                }
            }
        }
    }

    private void setGrid(int[][] gridArray, Grid grid) {
        for (int r = 0; r < gridArray.length; r++) {
            for (int c = 0; c < gridArray[r].length; c++) {
                if (gridArray[r][c] == 0) {
                    grid.setTile(new Position(r, c), new EmptyTile());
                } else if (gridArray[r][c] == 1) {
                    grid.setTile(new Position(r, c), new TetrisTile(TileColor.RED));
                }
            }
        }
    }

